package extend.org.compiere;

public class ExcelCell {

	private int c;
    private int r;
    private String v;
    private String type;

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }

    public int getR() {
        return r;
    }

    public void setR(int r) {
        this.r = r;
    }

    public String getV() {
        return v;
    }

    public void setV(String v) {
        this.v = v;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
	
}
